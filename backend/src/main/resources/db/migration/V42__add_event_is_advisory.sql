-- 안내성(예방·캠페인) 이벤트 표시 — 사건 이벤트와 분리
--
-- 산불은 알림의 ~85%가 실제 화재가 아니라 건조특보·소각금지·입산통제·예방캠페인 안내다.
-- 이 안내성 알림은 건기 내내 같은 시군구에서 며칠 간격으로 반복 발령돼, 지역앵커 유형 머지
-- (REGIONAL_TYPE, 윈도우 14일)에 그대로 태우면 시즌 전체가 하나로 체이닝된 blob(span 130일+)이
-- 되고, 소수의 실제 산불 이벤트에도 섞여 타임라인을 오염시킨다(실데이터 검증).
--
-- 해결: 산불 알림을 사건(INCIDENT) / 안내(ADVISORY)로 분류해, 안내성은 별도 롤링 이벤트
--       ("{시군구} 산불예방안내", is_advisory=true)로 모은다. 사건 머지·후보 검색은 is_advisory=false
--       이벤트만 대상으로 하여 안내성이 사건 버킷에 끼지 않게 격리한다.
--       산사태·홍수(안내=같은 호우 episode 조기경보, 강결합)·폭염은 적용하지 않는다.
ALTER TABLE disaster_events ADD COLUMN is_advisory BOOLEAN NOT NULL DEFAULT FALSE;
